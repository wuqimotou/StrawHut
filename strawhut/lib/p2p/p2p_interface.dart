/// P2P 服务接口
///
/// 定义 StrawHut Phase 6（P2P 网络功能）的服务契约。
/// 此接口为未来功能预留，当前版本未实现。
///
/// 架构位置：P2P 模块 → 服务接口
/// 计划实现技术：libp2p（dart-libp2p）或 Matrix SDK
///
/// 功能概述：
/// - 发布知识卡片到 P2P 网络（发布加密内容 + 公开元数据）
/// - 发现网络中的知识卡片（浏览公开元数据列表）
/// - 下载感兴趣的卡片（获取加密内容）
/// - 查看 P2P 网络状态
/// - 关闭 P2P 连接
///
/// 安全设计：
/// - 仅发布加密内容和公开元数据（不泄露密钥）
/// - 下载方仍需通过其他渠道获取密钥才能解密
/// - 网络层使用 TLS 加密传输
abstract class IP2PService {
  /// 发布知识卡片到 P2P 网络
  ///
  /// 将 .straw 文件的加密内容和公开元数据广播到网络。
  /// 不会发布密钥（.key 文件）。
  Future<void> publishToNetwork();

  /// 发现网络中的知识卡片
  ///
  /// 浏览其他用户发布的卡片公开元数据列表。
  /// 返回 DiscoveredCard 列表，包含标题、描述等信息。
  Future<void> discoverCards();

  /// 下载指定知识卡片
  ///
  /// 从 P2P 网络获取指定 cardId 的完整加密内容。
  /// 下载后仍需输入密钥才能解密。
  ///
  /// 参数：cardId - 卡片唯一标识
  Future<void> downloadCard();

  /// 获取 P2P 服务状态
  ///
  /// 返回当前网络连接状态、节点数量等信息。
  Future<void> getStatus();

  /// 关闭 P2P 连接
  ///
  /// 断开所有网络连接，释放 P2P 资源。
  Future<void> shutdown();
}
