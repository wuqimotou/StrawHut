/// 口令强度等级
///
/// 用于评估用户输入口令的安全强度。
/// 强度从低到高分为四个等级：
/// - [veryWeak]: 非常弱，不建议使用
/// - [weak]: 弱，仅满足最低要求
/// - [medium]: 中等，提供基本安全性
/// - [strong]: 强，推荐使用
///
/// 评估规则由 [PassphraseStrengthService] 实现。
enum PassphraseStrength {
  /// 非常弱
  ///
  /// 满足以下任一条件即为非常弱：
  /// - 长度小于 8 个字符
  /// - 为空或仅包含空白字符
  /// - 包含 6 个及以上连续相同字符
  /// - 包含连续数字序列（如 "123456" 或 "654321"）
  veryWeak,

  /// 弱
  ///
  /// 长度 >= 8 但不满足 medium 或 strong 的条件。
  weak,

  /// 中等
  ///
  /// 长度 >= 12 且同时包含字母和数字。
  medium,

  /// 强
  ///
  /// 长度 >= 16 且满足以下 3 个及以上条件：
  /// - 包含大写字母
  /// - 包含小写字母
  /// - 包含数字
  /// - 包含特殊字符
  strong,
}
